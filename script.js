let currentLang = localStorage.getItem('rongqi-lang') || 'zh';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('rongqi-lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelector('.lang-zh').style.opacity = lang === 'zh' ? '1' : '0.4';
  document.querySelector('.lang-en').style.opacity = lang === 'en' ? '1' : '0.4';
}

function initHeader() {
  const header = document.getElementById('header');
  window.addEventListener('scroll', () => {
    header.classList.toggle('scrolled', window.scrollY > 50);
  });
}

function initMobileMenu() {
  const toggle = document.getElementById('menuToggle');
  const links = document.querySelector('.nav-links');

  toggle.addEventListener('click', () => {
    toggle.classList.toggle('active');
    links.classList.toggle('open');
    document.body.style.overflow = links.classList.contains('open') ? 'hidden' : '';
  });

  links.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', () => {
      toggle.classList.remove('active');
      links.classList.remove('open');
      document.body.style.overflow = '';
    });
  });
}

function initReveal() {
  const reveals = document.querySelectorAll('.reveal');
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry, index) => {
      if (entry.isIntersecting) {
        setTimeout(() => entry.target.classList.add('visible'), index * 100);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15, rootMargin: '0px 0px -50px 0px' });

  reveals.forEach(el => observer.observe(el));
}

function initContactForm() {
  const form = document.getElementById('contactForm');
  form.addEventListener('submit', (e) => {
    e.preventDefault();
    const name = document.getElementById('name').value;
    const email = document.getElementById('email').value;
    const message = document.getElementById('message').value;
    const subject = encodeURIComponent(`Business Inquiry from ${name}`);
    const body = encodeURIComponent(`Name: ${name}\nEmail: ${email}\n\n${message}`);
    window.location.href = `mailto:D.irlaneMaah@gmail.com?subject=${subject}&body=${body}`;
    alert(translations[currentLang]['form.success']);
    form.reset();
  });
}

function initLangToggle() {
  document.getElementById('langToggle').addEventListener('click', () => {
    setLanguage(currentLang === 'zh' ? 'en' : 'zh');
  });
}

document.addEventListener('DOMContentLoaded', () => {
  setLanguage(currentLang);
  initHeader();
  initMobileMenu();
  initReveal();
  initContactForm();
  initLangToggle();
});
